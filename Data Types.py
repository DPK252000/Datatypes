"""Int"""
print(type(2))
print(type(287636))
print(type(-30))
"""Float"""
print(type(.52))
print(type(1.2))
print(type(.999))
"""String"""
print(type('hello'))
print(type('kiran'))
print(type('madhu'))
"""Bool"""
print(type(True))
print(type(False))
"""Tuple"""
p=1,2,3
print(type(p))
"""List"""
p=[1,6,9]
p.clear
print(type(p))
"""Set"""
print(type({1,2,3}))
print(type({9}))
"""Dict"""
p={3:'pavan'}
print(type(p))
p.clear
print(type(p))
print(p)
"""Complex"""
p=complex(9+9j)
print(p)
p=complex(9j)
print(p)
p=complex(6)
print(p)
"""Byte"""
print(type(b'pavan'))
print(type(b'hello'))
"""Bytearray"""
print(type(bytearray(b'hello')))
print(type(bytearray(b'hi')))




     


























































              